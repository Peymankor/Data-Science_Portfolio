
install.packages('nleqslv')
library(nleqslv)
fn <- function(x){
  c(-0.152 + 2*x[1]**(2*1)/(x[2] + x[1]**(2*1)*1),
    -0.0771 + 2*x[1]**(2*2)/(x[2] + x[1]**(2*2)*2),
    -0.0392 + 2*x[1]**(2*3)/(x[2] + x[1]**(2*3)*3),
    -0.0193 + 2*x[1]**(2*4)/(x[2] + x[1]**(2*4)*4),
    -0.00943 + 2*x[1]**(2*5)/(x[2] + x[1]**(2*5)*5))
}

nleqslv(c(0.1, 0.9), fn)
